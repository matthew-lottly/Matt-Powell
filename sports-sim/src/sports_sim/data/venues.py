def get_venue(sport: str, team_abbr: str) -> Venue | None:
    """Look up a venue by sport and team abbreviation."""
    registries = {
        "football": NFL_VENUES,
        "basketball": NBA_VENUES,
        "baseball": MLB_VENUES,
        "hockey": NHL_VENUES,
    }
    reg = registries.get(sport)
    if reg:
        return reg.get(team_abbr)
    return None
